                <h1 class="text-center header-text-color">Garam Chashma</h1>
                <p class="text-dark text-justify">
                    Garam Chashma(Hot Spring) is one of the many branch valleys of Chitral District, situated in the
                    extreme north-west of Pakistan.
                    It is one of the highest human settlements in the Hindukush ranges with an altitude of almost 2550
                    meters (8389 feet). The place is known for Hot spring, which is one of the tourists attraction
                    sites. It is also famous for its trout fish. The water flowing down through the length of the valley
                    is famous for fishing sport. The Lotkoh River (Garam Chashma River) running down from the lofty
                    peaks of the Hindu Kush is most suitable for the brown type of trout fish. Other features of the
                    area include snow-covered peaks, pleasant weather, natural springs, and more recently has remained
                    in the limelights for being a potential site of hydel-power generation. Besides tourists, people
                    suffering from skin diseases also visit the hot spring for treatment (not proven scientifically).It
                    is located in the North-West of Chitral at a distance of about forty-eight kilometres by road. It
                    shares international borders with Afghanistan being situated in the extreme North-West of Pakistan.
                    Dorāh Pass (Do Rāh) [14,940 feet (4,554 metres)] connects this part of Pakistan with Badakhshan,
                    the adjacent province of Afghanistan.</p>
                <h5 class="heading-h5">The Hot Spring</h5>
                <p class="text-justify">The spring water emerges from underground sulphur deposits making its
                    temperature rise above boiling point. For the same reason, the spring is also known as Sulphur
                    Spring. The spring is the warmest in the westward extension of the Himalayan Geothermal Belt. It
                    emerges from leucogranites of the Hindu Kush Range that date from 20–18 Ma. Reservoir temperatures
                    may be as high as 260 °C (500 °F). It is not clear whether the circulation of deep groundwater in
                    this region is driven by topography or by tectonic lateral stress</p>
